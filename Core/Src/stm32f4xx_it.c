/**
  * @brief This function handles USART6 global interrupt.
  */
char Receive[14];
void USART6_IRQHandler(void)
{
  /* USER CODE BEGIN USART6_IRQn 0 */

  /* USER CODE END USART6_IRQn 0 */
  HAL_UART_IRQHandler(&huart6);
  /* USER CODE BEGIN USART6_IRQn 1 */
	Receive[Usart_Cnt++]=abuffer;
	HAL_UART_Receive_IT(&huart6,(uint8_t*)&abuffer,1);
	if(Usart_Cnt == 14) 
	{
		extern _tx2_control_data control_data;
		Usart_Cnt = 0;
		if((Receive[0] == 'H') && (Receive[13] == 'J'))
		{
			extern a_1 ccc;
			ccc.data[0]=Receive[1];
			ccc.data[1]=Receive[2];
			ccc.data[2]=Receive[3];
			ccc.data[3]=Receive[4];
		control_data.forward_speed=ccc.aaa;
			ccc.data[0]=Receive[5];
			ccc.data[1]=Receive[6];
			ccc.data[2]=Receive[7];
			ccc.data[3]=Receive[8];
		control_data.zuoyou_speed=ccc.aaa;
			ccc.data[0]=Receive[9];
			ccc.data[1]=Receive[10];
			ccc.data[2]=Receive[11];
			ccc.data[3]=Receive[12];
		control_data.rotate_speed=ccc.aaa;
		}
	}
  /* USER CODE END USART6_IRQn 1 */
}

